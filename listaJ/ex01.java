package listaJ;

public class ex01 {

	public static void main(String[] args) {
		int cont = 0;
		int cont_dec = 100;
		System.out.println("Ordem crescente");
		while (cont <= 100) {
			System.out.println(cont);
			cont ++;
		}
		System.out.println("Ordem Decrescente");
		while (cont_dec != 0 ) {		
			System.out.println(cont_dec);
			cont_dec--;
		}	
	}
}
								